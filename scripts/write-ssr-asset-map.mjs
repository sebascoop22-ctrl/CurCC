/**
 * After `vite build`, maps HTML entries to emitted JS/CSS for Vercel SSR HTML.
 * Reads `dist/.vite/manifest.json` and writes:
 * - `server/ssr-asset-hashes.json` (debug/reference)
 * - `server/ssr-asset-hashes.ts` (runtime-safe for Node ESM on Vercel)
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const manifestPath = path.join(root, "dist", ".vite", "manifest.json");
const outPath = path.join(root, "server", "ssr-asset-hashes.json");
const outTsPath = path.join(root, "server", "ssr-asset-hashes.ts");

function toAbs(href) {
  if (!href) return "";
  return href.startsWith("/") ? href : `/${href}`;
}

function collectCssFromChunk(manifest, key, seen = new Set(), out = []) {
  if (!key || seen.has(key)) return out;
  seen.add(key);
  const chunk = manifest[key];
  if (!chunk) return out;
  for (const dep of chunk.imports ?? []) {
    collectCssFromChunk(manifest, dep, seen, out);
  }
  for (const href of chunk.css ?? []) {
    const abs = toAbs(href);
    if (abs && !out.includes(abs)) out.push(abs);
  }
  return out;
}

function bundleForHtmlKey(manifest, htmlFile) {
  const key = Object.keys(manifest).find((k) => k === htmlFile || k.endsWith(`/${htmlFile}`));
  if (!key) return null;
  const chunk = manifest[key];
  if (!chunk?.file) return null;
  const js = toAbs(chunk.file);
  const css = collectCssFromChunk(manifest, key);
  return { js, css };
}

function main() {
  if (!fs.existsSync(manifestPath)) {
    console.warn("write-ssr-asset-map: no dist/.vite/manifest.json — skip (run vite build first)");
    process.exit(0);
  }
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  const nightlife = bundleForHtmlKey(manifest, "nightlife.html");
  const clubDetail = bundleForHtmlKey(manifest, "club.html");
  const payload = { nightlife, clubDetail };
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, JSON.stringify(payload, null, 2), "utf8");
  const tsModule = `/**
 * Auto-generated by scripts/write-ssr-asset-map.mjs — do not edit.
 */
export const ssrAssetHashes = ${JSON.stringify(payload, null, 2)} as const;
`;
  fs.writeFileSync(outTsPath, tsModule, "utf8");
  console.log("write-ssr-asset-map:", outPath.replace(/\\/g, "/"), payload);
}

main();
